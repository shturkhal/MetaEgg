import SwiftUI

extension Font {
    static func suraBold(size: CGFloat) -> Font {
        return Font.custom("Sura-Bold", size: size)
    }
    static func suraRegular(size: CGFloat) -> Font {
        return Font.custom("Sura-Regular", size: size)
    }
}
